namespace Microsoft.StandardUI.Text
{
    /// <summary>
    /// Provides a set of predefined font weights as static property values.
    /// </summary>
    public static class FontWeights
    {
        /// <summary>
        /// Specifies a font weight value of 100.
        /// </summary>
        public static FontWeight Thin => new FontWeight(100);

        /// <summary>
        /// Specifies a font weight value of 200.
        /// </summary>
        public static FontWeight ExtraLight => new FontWeight(200);

        /// <summary>
        /// Specifies a font weight value of 300.
        /// </summary>
        public static FontWeight Light => new FontWeight(300);

        /// <summary>
        /// Specifies a font weight value of 350.
        /// </summary>
        public static FontWeight SemiLight => new FontWeight(350);

        /// <summary>
        /// Specifies a font weight value of 400.
        /// </summary>
        public static FontWeight Normal => new FontWeight(400);

        /// Specifies a font weight value of 500.
        /// </summary>
        public static FontWeight Medium => new FontWeight(500);

        /// <summary>
        /// Specifies a font weight value of 600.
        /// </summary>
        public static FontWeight SemiBold => new FontWeight(600);

        /// <summary>
        /// Specifies a font weight value of 700.
        /// </summary>
        public static FontWeight Bold => new FontWeight(700);

        /// <summary>
        /// Specifies a font weight value of 800.
        /// </summary>
        public static FontWeight ExtraBold => new FontWeight(800);

        /// <summary>
        /// Specifies a font weight value of 900.
        /// </summary>
        public static FontWeight Black => new FontWeight(900);

        /// <summary>
        /// Specifies a font weight value of 950.
        /// </summary>
        public static FontWeight ExtraBlack => new FontWeight(950);
    }
}
